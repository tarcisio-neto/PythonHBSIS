# Exercício 02 - Escreva um algoritmo para ler as dimensões de um retãngulo(base e altura), calcular e escrever a 
#                área do retângulo

print ('Digite a base do retângulo: ')
base_retangulo = int(input())

print ('Digite a altura do retângulo: ')
altura_retangulo = int(input())

area_retangulo = (base_retangulo * altura_retangulo)

print ('A área do retângulo é {}'.format(area_retangulo))